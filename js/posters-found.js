function buildPosters(posters, id) {

	for (var i = 0; i < posters.length; i++) {

		var poster = posters[i];
		var title = poster.title;
		var artist = poster.artist;
		var date = new Date(poster.date);
		date = date.toDateString()

		// build img attributes, fancybox classes
		var img = '<img src="' + poster.imgSrc + '">';
		var anchor = '<a href="' + poster.imgSrc + '" class="fancybox" rel="gallery" title=" '
		+ title + '"'
		+ '>' + img + '</a></div>';

		var nameHTML = '<div class="text-center callout"><h4>' 
		+ title + '</h4><p>'
		+ date + '<br><strong><em>' 
		+ artist 
		+ '</em></strong></p></div>';

		// create new column div with element
		var elem = document.createElement('div');
		elem.className = 'column';
		elem.innerHTML = anchor + nameHTML;

		$(id).append(elem);
	}
}


